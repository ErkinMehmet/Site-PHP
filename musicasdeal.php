 Ou bien quand on habituellement laisse quelque chose à côté, en espérant que quelqu'un la fasse pour nous, mais seulement en se rendant compte qu'il faut qu'on apprenne 
 de nouveau à vivre une vie indépendante, avec la force et le courage.</p>

<H4>Un elefante en el pesebre</H4>
<p>Los Caligaris sont très connus pour leurs productions de chansons féstives et heureuses, mais cette fois-ci, tout au contraire, ils ont écrit une chanson attristante
 et nostalgique. L'éléphant, apparemment n'appartient pas au 'pesebre', lequel est un endroit divin, donc il ne peut pas rester tout seul, en noyant son chagrin dans un verre 
 de whiskey avec des glaçons pour se rechauffer, en ayant plus de froid consécutivement. Ma phrase favorite de cette chanson est 'piano sin cuerdas, tango sin pasión': quand les 
 symboles des fêtes et du bonheur s'alourdissent et cessent de briller, on sait qu'on est dans le profondeur de l'angoisse. Le plus vous écoutez cette chanson, le plus vous pouvez 
 ressentir la tristesse de laquelle cette chanson s'imprègne.
</p>
